#include<iostream>
#include<math.h>
#include<limits.h>
#include<vector>
using namespace std;

int main() {
    int t;
    cin>>t;
    while(t--) {
        int n;
        cin>>n;
        vector<int> arr(n, 0);
        int sum = 0;
        for(int i=0; i<n; i++) {
            cin>>arr[i];
            sum += arr[i];
        }

        cout<<sum/n<<"\n";
    }
    return 0;
}


